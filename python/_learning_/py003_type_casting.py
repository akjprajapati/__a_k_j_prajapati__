# Type Casting = Convert the data type of a value to another data type.

x = 12    # Integer
y = 1.2   # Float
z = "12"  # String


# Convert all the values to string.
x = str(x)
y = str(y)
z = str(z)

print("X is "+x)
print("Y is "+y)
print("Z is "+z)


# Convert all the values to float.
y = float(y)
z = float(z)
x = float(x)

print("X is "+str(x))
print("Y is "+str(y))
print("Z is "+str(z))

# Convert all the values to integer.
x = int(x)
y = int(y)
z = int(z)

print("X is "+str(x))
print("Y is "+str(y))
print("Z is "+str(z))
