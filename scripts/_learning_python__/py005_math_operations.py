import math

pi = 3.14

x = 1.2
y = 2.3
z = 3.4

pi_negative = -3.14


# FInd the round figure of a fraction value.
print(round(pi))

# Find upper round number of a fraction value.
print(math.ceil(pi))

# Find lower round number of a fraction value.
print(math.floor(pi))

# Find the absolute value of a negative number.
print(abs(pi_negative))

# Find exponential value of a number and its power.
print(pow(pi,2))

# Find the square root of a number.
print(math.sqrt(pi))

# Find the biggest among multiple values.
print(max(x,y,z))

# Find the lowest among multiple values.
print(min(x,y,z))