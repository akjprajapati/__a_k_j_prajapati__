# Function with return statement.
def multiply(num1, num2):
    result = num1 * num2
    return result


print(multiply(10, 12))     # return statement requires 'print()' to display the result.


# Another way of doing this is.
def addition(val1, val2):
    return val1 + val2


result = addition(1, 2)
print(result)
